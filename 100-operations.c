/**
 * add - adds 2 integers
 * @a: term 1
 * @b: term 2
 * Return: result of addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substractes 2 integers
 * @a: term 1
 * @b: term 2
 * Return: result of substraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies 2 integers
 * @a: term 1
 * @b: term 2
 * Return: result of multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides 2 integers
 * @a: term 1
 * @b: term 2
 * Return: result of division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - a modulo b
 * @a: term 1
 * @b: term 2
 * Return: rest of division
 */
int mod(int a, int b)
{
	return (a % b);
}
